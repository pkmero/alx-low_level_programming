#include "main.h"
/**
 * _isdigit - a function to determine whether the character
 * is a digit or not
 *
 * @i: a variable with the input type int
 *
 * Return: returns 1 if i is a digit and 0 if else
 */
int _isdigit(int i)
{
	if ((i >= 48) && (i <= 57))
		return (1);
	else
		return (0);
	return (0);
}
